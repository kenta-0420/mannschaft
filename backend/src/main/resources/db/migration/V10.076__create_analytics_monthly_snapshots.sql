CREATE TABLE analytics_monthly_snapshots (
    id BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    month DATE NOT NULL,
    mrr DECIMAL(12,2) NOT NULL DEFAULT 0,
    arr DECIMAL(14,2) NOT NULL DEFAULT 0,
    arpu DECIMAL(10,2) DEFAULT NULL,
    ltv DECIMAL(12,2) DEFAULT NULL,
    nrr DECIMAL(8,4) DEFAULT NULL,
    quick_ratio DECIMAL(8,4) DEFAULT NULL,
    payback_months DECIMAL(6,2) DEFAULT NULL,
    total_users INT UNSIGNED NOT NULL DEFAULT 0,
    active_users INT UNSIGNED NOT NULL DEFAULT 0,
    paying_users INT UNSIGNED NOT NULL DEFAULT 0,
    new_users INT UNSIGNED NOT NULL DEFAULT 0,
    churned_users INT UNSIGNED NOT NULL DEFAULT 0,
    user_churn_rate DECIMAL(5,2) NOT NULL DEFAULT 0,
    revenue_churn_rate DECIMAL(5,2) NOT NULL DEFAULT 0,
    net_revenue DECIMAL(12,2) NOT NULL DEFAULT 0,
    ad_revenue DECIMAL(12,2) NOT NULL DEFAULT 0,
    report_sent BOOLEAN NOT NULL DEFAULT FALSE,
    report_sent_at DATETIME DEFAULT NULL,
    created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (id),
    UNIQUE KEY uk_month (month),
    CONSTRAINT chk_snapshot_month_first CHECK (DAY(month) = 1)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
